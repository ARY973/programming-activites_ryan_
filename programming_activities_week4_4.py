# This program prints all multiples of 5 from 5 to 95 using a while loop.

def print_multiples_of_five_while():
    """
    prints multiples of 5 from 5 to 95 using a while loop.
    """
    number = 5

    while number <=95:
        print(number)
        number += 5
print_multiples_of_five_while()
